#![feature(test)]
use aoc_2023_19::model::{Action, AocError, PartRange};
use aoc_2023_19::InputModel;
use rayon::prelude::*;

const INPUT: &str = include_str!("../data/input.txt");

fn part1(input: &InputModel) -> Result<String, AocError> {
    let wfs = &input.workflows;
    Ok(input
        .parts
        .par_iter()
        .filter(|p| wfs.evaluate(p) == Ok(Action::Accepted))
        .map(|p| p.rating())
        .sum::<u64>()
        .to_string())
}

fn part2(input: &InputModel) -> Result<String, AocError> {
    let start = PartRange::default();
    let wfs = &input.workflows;
    let result = wfs.evaluate_range(&start)?;
    Ok(result.iter()
        .map(|p| p.combinations())
        .sum::<u64>().to_string())
}

fn main() -> Result<(), AocError> {
    let input: InputModel = INPUT.parse::<InputModel>()?;
    let part1_result = part1(&input)?;
    println!("Part1: {}", part1_result);
    println!("--------------");
    let part2_result = part2(&input)?;
    println!("Part2: {}", part2_result);
    Ok(())
}

